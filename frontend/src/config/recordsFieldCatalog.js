/**
 * @param {string} table
 * @param {string} column
 * @param {string} dataType information_schema DATA_TYPE
 */
export function resolveFieldRow(table, column, dataType) {
  const cat = FIELD_CATALOG_BY_TABLE[table]?.[column]
  const qKey = `${table}.${column}`
  const base = {
    qKey,
    table,
    column,
    dataType: dataType || '',
    label: column,
    group: '—',
    kind: 'unknown',
  }
  if (!cat) {
    const dt = (dataType || '').toLowerCase()
    if (/int|decimal|float|double/.test(dt)) {
      return {
        ...base,
        kind: 'numeric',
        label: column,
        rngFamily: 'op',
        ranges: R([0, 1], [0, 1], [0, 1]),
        decimals: /decimal|float|double/.test(dt) ? 2 : 0,
        unit: '',
        note: '库中存在该数值列，脚本中区间未单独建模，参考区间仅为占位；调节仍会下发 min/max',
      }
    }
    return { ...base, kind: 'unknown', note: '脚本未暴露该列的生成参数' }
  }
  return { ...base, ...cat }
}

export function qualityLabel(q) {
  const m = { high: '高', medium: '中', low: '低' }
  return m[q] || q
}
